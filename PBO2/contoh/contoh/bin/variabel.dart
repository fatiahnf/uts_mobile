//metode utama kata kunci nya adalah main
//metode void adalah metode yang tidak mengembalikan nilai apapun setelah di
//eksekusi artinya dia hanya melakukan sebuah proses
void main() {
  // membuat variabel dengan tipe data
  // Variabel adalah sebuah simbol yang digunakan untuk menyimpan nilai.
  // Sedangkan tipe data adalah jenis nilai yang akan kita simpan.
  // Tipe data dasar pada Dart dibagi menjadi tiga macam:
  // Tipe data Angka (Number): int, double
  // Tipe data teks: String
  // Tipe data boolean: bool

  String nama = "Fatia";
  int umur = 20;
  double tinggi = 150.43;
  bool isMenikah = false;

  // membuat veriabel dengan kata kunci var
  var alamat = "Banjarmasin, Indonesia";

  // Kita sudah mengenal fungsi print() yang digunakan untuk menampilkan output ke dalam console.
  // mencetak variabel
  print("Nama saya $nama. Umur $umur tahun. Tinggi sekitar $tinggi cm.");
  print("Menikah: $isMenikah");
  print("Alamat: $alamat");
}
